    if $game_variables[1] < 0
      cmd = pbMessage("Cancel ability change?",["Yes", "No"],1)
      if cmd <= 0
        return false
        break
      end
    else
      loop do
        pkmn = pbGetPokemon(1)
        abilitylist = []
        abilities = []
        for ability in pkmn.species_data.abilities
          i = ability.to_s
          abilityname = GameData::Ability.get(i).real_name
          abilities.push(abilityname)
