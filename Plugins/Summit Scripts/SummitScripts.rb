          listprice.insert(1, ",")
          listing << " - $" << listprice
          abilitylist.push(listing)
        end
        for ability in pkmn.species_data.hidden_abilities
          i = ability.to_s
          abilityname = GameData::Ability.get(i).real_name
          abilities.push(abilityname)
          listing = abilityname.clone
          price = 3000
