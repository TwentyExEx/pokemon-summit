        if cmd == -1
          break
        else
          @chosenability = abilities[cmd]
          if pkmn.species_data.abilities.include?(@chosenability.upcase.to_sym)
            price = 2000
          else
            price = 3000
          end
          listprice = price.to_s.clone
          listprice.insert(1, ",")
          if $Trainer.money < price
            pbMessage("\\rSorry, you don't have enough money.")
