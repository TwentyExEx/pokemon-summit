      for stat in $allstats
        if stat != :HP
          statname = stat.clone
          statname = statname.to_s.downcase
          statname.gsub!(/_/, " ")
          statname.gsub!(/(\w+)/) {|word| word.capitalize}
          stats.push(statname)
        end
      end
      stats << "None"
      loop do
        cmd = pbMessage(_INTL("\\rWhich stat should this nature raise?"),stats,-1)
